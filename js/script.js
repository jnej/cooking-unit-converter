//oz converted to ml//

var volume = function(ml){return ml*29.57;};

var ml = parseFloat(prompt("Enter volume in fluid oz"));

var result = volume(ml);

alert(result);

//kg converted to lb//

var weight = function(kg){return kg*2.2;};

var lb = parseFloat(prompt("Enter weight in kg"));

var result = weight(lb);

alert(result);
